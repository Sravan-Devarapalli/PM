CREATE TABLE [dbo].[TimeEntryHistory]
    (
      [Id] INT IDENTITY(1, 1)
               NOT NULL ,
      [TimeEntryId] INT NOT NULL ,
      [PersonId] INT NOT NULL ,
      [ChargeCodeId] INT NOT NULL ,
      [ChargeCodeDate] DATETIME NOT NULL ,
      [Note] NVARCHAR(1000) NOT NULL ,
      [ForecastedHours] REAL NOT NULL ,
      [IsCorrect] BIT NOT NULL ,
      [IsAutoGenerated] BIT NOT NULL ,
	  [OldHours]    REAL NOT NULL,
      [ActualHours] REAL NOT NULL ,
      [CreateDate] DATETIME NOT NULL ,
      [ModifiedDate] DATETIME NOT NULL ,
      [ModifiedBy] INT NOT NULL ,
      [IsChargeable] BIT NOT NULL ,
      [ReviewStatusId] INT NOT NULL ,
      [ActivityTypeId] INT NOT NULL ,
      [SessionID] INT NOT NULL ,
      CONSTRAINT PK_TimeEntryHistory_TimeEntryIdAndId PRIMARY KEY CLUSTERED
        ( TimeEntryId ASC, Id ASC )
    );

